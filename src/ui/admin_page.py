    st.divider()

    # =========================================
    # ‡∏™‡πà‡∏ß‡∏ô‡∏ó‡∏µ‡πà 1: AI Rule Extractor (‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Å‡∏é‡πÉ‡∏´‡∏°‡πà)
    # =========================================
    st.markdown("### 1. AI Rule Extractor (‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Å‡∏é‡πÉ‡∏´‡∏°‡πà)")
