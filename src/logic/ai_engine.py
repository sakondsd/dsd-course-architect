def get_api_key():
    # 1. ‡∏•‡∏≠‡∏á‡∏´‡∏≤‡∏à‡∏≤‡∏Å .env ‡∏Å‡πà‡∏≠‡∏ô
    key = os.getenv("GOOGLE_API_KEY")
