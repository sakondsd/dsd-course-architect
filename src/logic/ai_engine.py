def get_llm():
    api_key = get_api_key()
    if not api_key:
        raise ValueError("‚ùå ‡πÑ‡∏°‡πà‡∏û‡∏ö Google API Key! ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡πÉ‡∏ô .env ‡∏´‡∏£‡∏∑‡∏≠ Secrets")
    
    return ChatGoogleGenerativeAI(
        model="gemini-2.5-flash", 
        google_api_key=api_key,
        temperature=0.3
    )

